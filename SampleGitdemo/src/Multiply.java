
public class Multiply {
	
	public static void main(String[] args) {
		
		//Declare variables and floating point values
		float p=2.6f;
		float s=1.5f;
		
		//Multiply
		float d=p*s;
		
		//Print the result
		System.out.println("The product is: " + d);
		
		
		
		
		
	}

}

//Time complexity - O(1) - doing constant operations
//Space complexity - O(1)